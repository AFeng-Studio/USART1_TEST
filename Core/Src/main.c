// 重写_write函数，用于printf重定向到USART1
int _write(int file, char *ptr, int len)
{
  // 只处理标准输出(stdout, 文件描述符1)和标准错误(stderr, 文件描述符2)
  if (file != 1 && file != 2) {
    return -1;
  }

    // 关键：等待上一次 DMA 传输完成（gState 变为 READY）
  while (huart1.gState != HAL_UART_STATE_READY) {
    // 可选：添加超时判断，避免死等（比如电机控制场景，超时返回错误）
    static uint32_t timeout = 0;
    if (++timeout > 100000) { // 约 1ms（72MHz 主频）
      timeout = 0;
      return -1; // 超时失败
    }
  }

  // 使用DMA式UART传输发送数据
  if (HAL_UART_Transmit_DMA(&huart1, (uint8_t*)ptr, len) != HAL_OK) {
    return -1; // 传输错误，返回-1表示失败
  }

  return len; // 返回成功发送的字节数
}

